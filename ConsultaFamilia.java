private void CargarFamilias(){
    
    java.util.ArrayList<Familia> lista = Familia.ListaFamilias();
		for(Familia familia : lista)
			combo_familias.addItem(familia);

}
Familia family;

private void CargarPanelFamilia(){
    family = (Familia)combo_familias.getSelectedItem();
    this.calle_txt.setText(family.Calle);
    this.num_integrantes_txt.setText(Integer.toString(family.No_Integrantes));
    this.colonia_combo.setEditable(true);
    this.municipio_combo.setEditable(true);
    this.colonia_combo.setSelectedItem(family.Colonia);
    this.municipio_combo.setSelectedItem(family.Municipio);
    this.colonia_combo.setEditable(false);
    this.municipio_combo.setEditable(false);
    this.municipio_combo.setSelectedItem(family.Municipio);
    this.telefono_txt.setText(family.Telefono);
    
    
    
}
private void LimpiarPanelFamilia(){
    this.combo_familias.removeItem(combo_familias.getSelectedItem());
    this.calle_txt.setText("");
    this.num_integrantes_txt.setText("");
    this.colonia_combo.setEditable(true);
    this.colonia_combo.setSelectedItem("");
    this.municipio_combo.setEditable(true);
    this.municipio_combo.setSelectedItem("");
    this.telefono_txt.setText("");
    
}


